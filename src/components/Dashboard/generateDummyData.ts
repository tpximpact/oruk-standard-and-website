import sharedDefinitions from './sharedDefinitions.json'

interface GenerateParams {
  numRows: number
  rowsPerPage: number
  failEveryNRows: number
}

interface DataRow {
  name: { value: string }
  comment: { value: string }
  service: { value: string; url: string }
  developer: { value: string; url: string }
  publisher: { value: string; url: string }
  schemaVersion: { value: string }
  statusIsUp: { value: number }
  statusIsValid: { value: number }
  statusOverall: { value: number }
  lastTested: { value: string; url: string }
}

export const generate = ({ numRows, rowsPerPage, failEveryNRows }: GenerateParams) => ({
  NB: `This is dummy data generated by invoking generate({numRows: ${numRows}, rowsPerPage:${rowsPerPage}, failEveryNRows:${failEveryNRows}})`,
  service: {},
  definitions: generateDefinitions({
    rowsPerPage: rowsPerPage
  }),
  data: generateData({
    numRows: numRows,
    failEveryNRows: failEveryNRows
  })
})

export const generateDefinitions = ({ rowsPerPage }: { rowsPerPage: number }) => {
  const definitions: any = Object.assign({}, sharedDefinitions)
  Object.keys(definitions.views).map((viewKey: string) => {
    definitions.views[viewKey].rowsPerPage = rowsPerPage
  })
  return definitions
}

export const generateData = ({
  numRows,
  failEveryNRows
}: {
  numRows: number
  failEveryNRows: number
}): DataRow[] => {
  const data = Array(numRows)
    .fill(null)
    .map((_, n) => {
      const rowNum = n + 1
      const fail = rowNum % failEveryNRows === 0
      return generateDataRow(rowNum, fail)
    })
  return data
}

const padRowNum = (rowNum: number): string => (rowNum <= 9 ? '0' + rowNum : String(rowNum))

export const generateDataRow = (rowNum: number, fail: boolean): DataRow => {
  return {
    name: {
      value: 'Dummy service ' + padRowNum(rowNum)
    },
    comment: {
      value: lipsum(rowNum)
    },
    service: {
      value: 'example.com',
      url: 'http://example.com/' + rowNum + '.json'
    },
    developer: {
      value: 'DeveloperCo',
      url: 'https://example.com/developer'
    },
    publisher: {
      value: 'Local Authority ' + rowNum,
      url: 'https://example.com/localauthority'
    },
    schemaVersion: {
      value: rowNum < 9 ? '1.0' : '3.0'
    },
    statusIsUp: {
      value: 1
    },
    statusIsValid: {
      value: fail ? 0 : 1
    },
    statusOverall: {
      value: fail ? 0 : 1
    },
    lastTested: {
      value: '2024-08-06T18:' + padRowNum(rowNum) + ':07',
      url: '/developers/dashboard/' + rowNum
    }
  }
}

